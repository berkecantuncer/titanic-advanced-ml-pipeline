    """
    Gradio arayüzünden gelen ham verileri alır, tahmin yapar ve sonucu yorumlar.
    """

    # 1. Feature Engineering: FamilySize hesaplama
    family_size = sibsp + parch + 1

    # 2. Yeni veri girişi için DataFrame oluşturma
    # Not: Sütun isimleri ve sırası, modelin beklediği özelliklerle aynı olmalıdır.
