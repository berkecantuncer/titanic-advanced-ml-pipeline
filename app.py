    # 4. Sonuç Metnini Oluşturma
    if prediction == 1:
        result_text = f"✅ **HAYATTA KALIR** (Hayatta Kalma Olasılığı: %{survival_proba:.2f})"
        color = "green"
    else:
        result_text = f"❌ **ÖLÜR** (Ölme Olasılığı: %{death_proba:.2f})"
        color = "red"

    # HTML ile renklendirilmiş çıktı döndürme
    html_output = f"<h1 style='color: {color}; text-align: center;'>{result_text}</h1>"
    html_output += f"<p style='text-align: center; font-size: 1.2em;'>Detay: Hayatta Kalma: %{survival_proba:.2f} | Ölüm: %{death_proba:.2f}</p>"

    return html_output

