all_kfold_metrics = {}

# ROC çizimi için figür hazırlığı
plt.figure(figsize=(10, 8))
plt.title('10-Fold Çapraz Doğrulama: Ortalama ROC Eğrileri')

# 2. Her Algoritma İçin K-Fold Döngüsü
for name, pipeline in classifier_pipelines.items():

    fold_acc, fold_prec, fold_rec, fold_f1, fold_spec = [], [], [], [], []
    fold_cms = []

    # ROC için gerekli listeler
    tprs = []
    base_fpr = np.linspace(0, 1, 101)

    for train_index, test_index in skf.split(X, y):
        X_train_kf, X_test_kf = X.iloc[train_index], X.iloc[test_index]
        y_train_kf, y_test_kf = y.iloc[train_index], y.iloc[test_index]

        pipeline.fit(X_train_kf, y_train_kf)
        y_pred_kf = pipeline.predict(X_test_kf)

        # Metrikleri hesapla
        cm_kf = confusion_matrix(y_test_kf, y_pred_kf)
        fold_cms.append(cm_kf)
        fold_acc.append(accuracy_score(y_test_kf, y_pred_kf))
        fold_prec.append(precision_score(y_test_kf, y_pred_kf, zero_division=0))
        fold_rec.append(recall_score(y_test_kf, y_pred_kf))
        fold_f1.append(f1_score(y_test_kf, y_pred_kf))

        tn, fp, fn, tp = cm_kf.ravel()
        fold_spec.append(tn / (tn + fp) if (tn + fp) > 0 else 0)

        # ROC / AUC Hesaplama (Olasılık tahmini varsa)
        if hasattr(pipeline.named_steps['classifier'], 'predict_proba'):
            y_proba_kf = pipeline.predict_proba(X_test_kf)[:, 1]
            fpr, tpr, _ = roc_curve(y_test_kf, y_proba_kf)
            # Eğriyi interpole et (ortalama almak için)
            tpr_interp = np.interp(base_fpr, fpr, tpr)
            tpr_interp[0] = 0.0
            tprs.append(tpr_interp)

    # Ortalamaları hesapla
    avg_tpr = np.mean(tprs, axis=0) if tprs else None
    avg_auc = auc(base_fpr, avg_tpr) if avg_tpr is not None else None

    all_kfold_metrics[name] = {
        'Accuracy': np.mean(fold_acc),
        'Precision': np.mean(fold_prec),
        'Recall': np.mean(fold_rec),
        'F1-Score': np.mean(fold_f1),
        'Specificity': np.mean(fold_spec),
        'ROC AUC': avg_auc,
        'Mean_CM': np.mean(fold_cms, axis=0).astype(int)
    }

    # ROC Eğrisini Grafiğe Ekle
    if avg_tpr is not None:
        plt.plot(base_fpr, avg_tpr, label=f'{name} (Ort. AUC = {avg_auc:.4f})')

